example = "Прекрасно" #Присвоение в переменную exemple строки - "Прекрасно"
print(example[0]) #Вывод первого символа 
print(example[-1]) #Вывод последнего символа
print(example[4:9]) #Вывод второй половины строки с нечетным количеством символов
print(example[::-1]) #Вывод строки в обратном порядке
print(example[1:9:2]) #Вывод каждого второго символа




